    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        String nome = txtNomeTurma.getText();
        String turno = txtTurnoTurma.getText();
        String obs = txtObs.getText();
        
        TurmaModel turma = new TurmaModel();
        
        if(!nome.equals("") && !turno.equals("")){
            turma.setNome_turma(nome);
            turma.setTurno_turma(turno);
            turma.setObs_turma(obs);
            boolean inseriu = dao.insertTurma(turma);
            if(inseriu == true){
                JOptionPane.showMessageDialog(null,"Inserido com sucesso.");
            }else {
                JOptionPane.showMessageDialog(null,"Erro ao inserir.");
            }
            txtNomeTurma.setText("");
            txtTurnoTurma.setText("");
            txtObs.setText("");
        } else {
            JOptionPane.showMessageDialog(null,"Há campos obrigatórios não preenchidos.");
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

