
package model;

/**
 *
 * @author Beatr
 */
public class TurmaModel {
    private int cod_turma;
    private String nome_turma;
    private String obs_turma;
    private String turno_turma;

    /**
     * @return the cod_turma
     */
    public int getCod_turma() {
        return cod_turma;
    }

    /**
     * @param cod_turma the cod_turma to set
     */
    public void setCod_turma(int cod_turma) {
        this.cod_turma = cod_turma;
    }

    /**
     * @return the nome_turma
     */
    public String getNome_turma() {
        return nome_turma;
    }

    /**
     * @param nome_turma the nome_turma to set
     */
    public void setNome_turma(String nome_turma) {
        this.nome_turma = nome_turma;
    }

    /**
     * @return the obs_turma
     */
    public String getObs_turma() {
        return obs_turma;
    }

    /**
     * @param obs_turma the obs_turma to set
     */
    public void setObs_turma(String obs_turma) {
        this.obs_turma = obs_turma;
    }

    /**
     * @return the turno_turma
     */
    public String getTurno_turma() {
        return turno_turma;
    }

    /**
     * @param turno_turma the turno_turma to set
     */
    public void setTurno_turma(String turno_turma) {
        this.turno_turma = turno_turma;
    }
    
    
}
